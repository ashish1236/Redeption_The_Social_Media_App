package com.menga.Redeption.Models;

public class SpaceModel {
   private String Spaceddto;
   private String Spacedby;
   private String Spaceid;
   private long Spacedat;
   public SpaceModel() {
   }

   public String getSpaceddto() {
      return Spaceddto;

   }

   public void setSpaceddto(String spaceddto) {
      Spaceddto = spaceddto;
   }

   public String getSpacedby() {
      return Spacedby;
   }

   public void setSpacedby(String spacedby) {
      Spacedby = spacedby;
   }

   public String getSpaceid() {
      return Spaceid;
   }

   public void setSpaceid(String spaceid) {
      Spaceid = spaceid;
   }

   public long getSpacedat() {
      return Spacedat;
   }

   public void setSpacedat(long spacedat) {
      Spacedat = spacedat;
   }
}
